inteiro1= int(input("Digite o primeiro inteiro: "))
inteiro2 = int(input("Digite o segundo inteiro: "))
real = float(input("Digite um número real: "))

valor1 = (2 * inteiro1) * (inteiro2 / 2)
valor2 = (3 * inteiro1) + real
valor3 = real ** 3

print(f"O produto do dobro do primeiro com metade do segundo é {valor1}.")
print(f"A soma do triplo do primeiro com o terceiro é {valor2}.")
print(f"O terceiro elevado ao cubo é {valor3}.")
